package Operator;

public class CompareoperatorDemo {
    public static void main(String[] args) {
        //比较运算符：== != > >= < <=
        //关系运算符的结果都是boolean类型，要么是true,要么是false
        //以==为例
        int a = 10;
        int b = 10;
        int c = 20;
        System.out.println(a == b);
        System.out.println(a == c);
    }
}
